 function XunH(){
     if(ind==5){
         $(".banner1>ul>li").eq(0).css({"background":"#dfbfd1"}).siblings().css({"background":"#ccc"})
     }else{
         $(".banner1>ul>li").eq(ind).css({"background":"#dfbfd1"}).siblings().css({"background":"#ccc"})
     }
     //document.body.clientWidth
    var n = document.body.clientWidth
    $(".b").css({"width":n+"px"})
    console.log(document.body.clientWidth)
         var marginLeft=ind*n;
     
    $(".sml").css({"margin-left":-marginLeft})
 }

function sml(){
    if(ind!=5){
        ind++;
        $(".sml").css({"transition":"0.3s"})
        XunH()
    }else{
        $(".sml").css({"transition":"0s"})
            console.log(ind)
        ind=0;
        
        setTimeout('XunH();',50)
        setTimeout('$(".sml").css({"transition":"0.3s"});ind=1;XunH()',100)
    } 
}

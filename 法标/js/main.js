function w1Focus(a, b, c) { //元素获得焦点的时候
    //如果a的value值等于空，则改变b的内容为c
    switch (a.value) {
        case "":
            b.className = "text-muted";
            b.innerHTML = c;
            break;
    };
};

function w1Blur(a, b, c, d, e, f) { //元素失去焦点的时候
    //如果a的value值判断等于空，如果为空，则改变b的内容为c，并返回否
    if (a.value == "") { //判断
        b.className = "text-danger";
        b.innerHTML = c;
        return false;
    }
    else if (d) { //正则表达式
        //否则验证是否匹配d的判断，如果匹配，则改变b的内容为e，并返回否
        b.className = "text-danger";
        b.innerHTML = e;
        return false;
    }
    else {
        //以上都不符合再执行，b的内容改变为f，并返回是，然后通过验证
        b.className = "text-success";
        b.innerHTML = f;
        return true;
    };
};
